"""
Di Python ada 3 tipe data angka yaitu :
- int atau integer
- float
- complex
"""

# int -> singkatan dari integer, tipe data ini bernilai bilangan bulat yang bisa bernilai positif atau negatif
# untuk penulisannya bisa dilihat di file tipedata.py
angka0 = -5
angka1 = 2

print(angka0)
print(angka1)

# penulisan int seperti dibawah ini boleh
angka = int(10)
print(angka)

#? Apa bedanya dengan penulisan yang atas..?
#* Bedanya adalah ketika kita menggunakan yang bawah, kita bisa merubah nilai dari awalan str menjadi int
#* Bila dilihat sekilah memang tidak terlihat bedanya, tetapi penulisan yang bawah punya keunggunalan tersendiri


# float -> tipe data yang bernilai bilangan pecahan yang bisa positfi atau negatif
# float pasti ada koma, tetapi di dunia pemrograman penulisan angka dibelakang koma diganti dengan tanda "."
# untuk penulisannya bisa dilihat di file tipedata.py
phi = 3.14
ipk = 29.5

print(phi)
print(ipk)

# penulisan float seperti dibawah ini boleh
angka_f = float(15.5)
print(angka_f)





